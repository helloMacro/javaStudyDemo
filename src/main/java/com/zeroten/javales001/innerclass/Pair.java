package com.zeroten.javales001.innerclass;

public class Pair {
    private int minValue;
    private int maxValue;

    public Pair(int min, int max) {
        this.minValue = min;
        this.maxValue = max;
    }

    public int getMinValue() {
        return minValue;
    }

    public int getMaxValue() {
        return maxValue;
    }
}
